public class HellsEye extends ArmorAbility {

    {
        baseChargeUse = 35f;
    }


    @Override
    public int icon() {
        return HeroIcon.HELLS_EYE;
    }

    @Override
    public Talent[] talents() {
        return new Talent[]{Talent.ALL_SEEING, Talent.DIVINE_CONNECTION, Talent.CHAOS_WITHIN, Talent.HEROIC_ENERGY};

    }



    @Override
    protected void activate(ClassArmor armor, Hero hero, Integer target) {

        hero.sprite.operate(hero.pos);
        Sample.INSTANCE.play(Assets.Sounds.CHARGEUP);

        armor.charge -= chargeUse(hero);
        armor.updateQuickslot();
        Invisibility.dispel();
        hero.spendAndNext(Actor.TICK);

        ArrayList<Integer> spawnPoints = new ArrayList<>();
        for (int i = 0; i < PathFinder.NEIGHBOURS8.length; i++) {
            int p = hero.pos + PathFinder.NEIGHBOURS8[i];
            if (Actor.findChar(p) == null && (Dungeon.level.passable[p] || Dungeon.level.avoid[p])) {
                spawnPoints.add(p);
            }
        }


        ArrayList<Integer> respawnPoints = new ArrayList<>();

        for (int i = 0; i < PathFinder.NEIGHBOURS8.length; i++) {
            int p = hero.pos + PathFinder.NEIGHBOURS8[i];
            if (Actor.findChar( p ) == null && Dungeon.level.passable[p]) {
                respawnPoints.add( p );
            }
        }

        int index = Random.index( respawnPoints );

        HellEye mob = new HellEye();
        mob.summonEye(hero);
        GameScene.add( mob );
        mob.pos = Random.element(spawnPoints);
        ScrollOfTeleportation.appear( mob, respawnPoints.get( index ) );

        respawnPoints.remove( index );


        }

    }











