public class CM01 extends Mob implements Callback {

	private static final float TIME_TO_ZAP	= 1f;
	
	{
		spriteClass = CM01Sprite.class;
		
		HP = HT = 20;
		defenseSkill = 8;
		
		EXP = 6;
		maxLvl = 13;
		
		loot = Generator.Category.SCROLL;
		lootChance = 0.25f;
		
		properties.add(Property.ELECTRIC);
		properties.add(Property.INORGANIC);
	}
	
	@Override
	public int damageRoll() {
		return Random.NormalIntRange( 2, 8 );
	}
	
	@Override
	public int attackSkill( Char target ) {
		return 11;
	}
	
	@Override
	public int drRoll() {
		return Random.NormalIntRange(  3, 7 );
	}
	
	@Override
	protected boolean canAttack( Char enemy ) {
		return new Ballistica( pos, enemy.pos, Ballistica.MAGIC_BOLT).collisionPos == enemy.pos;
	}

	
	@Override
	protected boolean doAttack( Char enemy ) {

		if (Dungeon.level.distance(pos, enemy.pos) <= 1) {

			return super.doAttack(enemy);

		} else {

			spend( 1f );
			int dmg = Random.NormalIntRange(1, 4);
			zap();
			return super.doAttack(enemy);
		}
	}

	private void zap()
	{

		Ballistica trajectory = new Ballistica(pos, enemy.pos, Ballistica.STOP_TARGET);

		for (int i : trajectory.subPath(0, trajectory.dist)){
			GameScene.add(Blob.seed(i, 5, Freezing.class));
		}
		GameScene.add(Blob.seed(trajectory.collisionPos, 15, Freezing.class));


	}

	public void onZapComplete(){
		zap();
		next();
	}

	@Override
	public void call() {
		next();
	}
	{
		immunities.add( Freezing.class );
	}
	{
		immunities.add( Chill.class );
	}
}
